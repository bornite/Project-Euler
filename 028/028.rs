fn sum_of_numbers_on_diagonals(n: u64) -> u64 {
	let sum = (1..n)
        .map(|i| {
            let n = 2 * i + 1;
            4 * n * n - 6 * n + 6
        })
        .sum::<u64>() + 1;
	return sum;
}

